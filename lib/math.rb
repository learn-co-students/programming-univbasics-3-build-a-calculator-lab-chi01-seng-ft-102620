def addition(num1, num2)
 sum = num1 + num2
 sum
end

def subtraction(num1, num2)
 dif = num1 - num2
 dif
end

def division(num1, num2)
 dividend = num1 / num2
 dividend
end

def multiplication(num1, num2)
 product = num1 * num2
 product
end

def modulo(num1, num2)
 a = num1 % num2
 a
end

def square_root(num)
 b = Math.sqrt (num)
 b
end