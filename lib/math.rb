def addition(num1, num2)
  sum = num2 + num1
  sum
end

def subtraction(num1, num2)
  subtract = num1 - num2
  subtract
end

def division(num1, num2)
  divide = num1 / num2
  divide
end

def multiplication(num1, num2)
  mult = num1 * num2
end

def modulo(num1, num2)
  mod = num1 % num2
  mod
end

def square_root(num)
  root = Math.sqrt(num)
  root
end